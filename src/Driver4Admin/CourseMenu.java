    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String course_id = CourseId.getText();
        Check.checkCourse(course_id);
        if(Check.getX()==1){    
            try {
                Connection conn = ConnectionBuilder.getConnection();
                String sql = "SELECT * FROM Course WHERE course_id LIKE ?";
                PreparedStatement pstm = conn.prepareStatement(sql);
                pstm.setString(1, course_id);
                ResultSet rs = pstm.executeQuery();

                if (rs.next()) {
                    CourseName2.setText(rs.getString("course_name"));
                    Topic2.setText(rs.getString("course_topic"));
                }else{
                    CourseName2.setText("-");
                    Topic2.setText("-");
                    JOptionPane.showMessageDialog(null,"Cannot Found ID", "Warning",JOptionPane.ERROR_MESSAGE);
                }
                conn.close();  
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Error.", "Warning",JOptionPane.ERROR_MESSAGE);
            } catch (Exception ex){
                JOptionPane.showMessageDialog(null,"Please enter ID number.", "Warning",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

